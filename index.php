    $('#characterLeft').text('140 karakterer igjen');
    $('#message').keydown(function () {
        var max = 140;
        var len = $(this).val().length;
        if (len >= max) {
            $('#characterLeft').text('Du har nådd maks antall karakterer i meldingen');
            $('#characterLeft').addClass('red');
            $('#btnSubmit').addClass('disabled');            
        } 
